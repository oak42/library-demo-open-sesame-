package com.ackerley.library.common.persistence;

import java.util.List;

public interface CRUDMapper<T> {
    int createOne(T entity);           //entity id field is required, 其他optional...    null field insert is ok...

    T retrieveOneByID(String id);
    T retrieveOneByID(T entityWithID);  //也是 凭 id 来 get one...
    List<T> retrieveList(T entityFilter);//non-null的field起到filter作用...

    int updateOneByID(T entityWithID);    //凭 id 来 update...  null field update is ok...
    int updateNonBlankByID(T entityWithID);    //凭 id 来 update...  only non-blank object field is updated ...

    //实体表 为 逻辑/软删除；关系表 为 物理/硬删除
    //之所以 使用T 而非String，是因为想用#{DEL_FLAG_DELETED}...
    int deleteOneByID(T entityWithID);    //凭 id 来 update...  null field update is ok...
}
